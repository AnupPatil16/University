public class Student {

	private String sName, sDept;
	private int iId;

	public Student(String sName, String sDept, int iID) {
		this.sName = sName;
		this.sDept = sDept;
		this.iId = iID;

	}

	public String getsName() {
		return sName;
	}

	public String getsDept() {
		return sDept;
	}

	public int getiId() {
		return iId;
	}

	
	
}
